public class Calculadora{
	//Funcion add(+)
	public static int add(int a, int b){
		return a + b;
	}
	//Funcion sub(-)
	public static int sub(int a, int b){
		return a - b;
	}
	//Funcion div(/)
	public static int div(int a, int b){
		return a / b;
	}
	//Funcion mul(*)
	public static int mul(int a, int b){
		return a*b;
	}
	//Funcion mod(%)
  public static int mod(int a, int b) {
		return a % b;
	}
}
